				<strong><u>Events\Letters</u></strong>
				<ul>


				<li>On November 5th, 2012, Occupy the SEC and OWS-Alternative Banking Working Group <a href="http://www.occupythesec.org/files/MMF_Letter_Nov52012.pdf">submitted a letter to the S.E.C. and the Department of the Treasury</a>, asking for increased transparency and public involvement in the ongoing formulation of money market fund regulations. 
</li>

					<li>On October 11, 2012, Occupy the SEC <a href="http://www.occupythesec.org/files/OSEC-Volcker-Alternative-Comment-Letter.pdf">submitted issued a <a href="http://occupythesec.nycga.net/2012/10/11/wells-fargo-hud-mortgages-the-doj-pursues-another-big-bank-fraud/">press release</a> calling for a criminal investigation against Wells Fargo for alleged FHA mortgage fraud. 
</li>

					<li>On September 7th, 2012, Occupy the SEC <a href="http://www.occupythesec.org/files/OSEC-Volcker-Alternative-Comment-Letter.pdf">submitted a comment letter</a> to the Congressional House Financial Services Committee, which had <a href="http://financialservices.house.gov/news/documentsingle.aspx?DocumentID=306143">requested public comment</a> on possible alternatives to the Volcker Rule. 
</li>

					<li>On July 31st, 2012 Occupy the SEC <a href="http://www.occupythesec.org/files/JOBSAct.pdf">submitted a comment letter</a> to the SEC in connection with the agency's anticipated implementation of the deceptively-titled Jumpstart Our Business Startups (JOBS) Act.</li>

					<li>On July 25th, 2012 Occupy the SEC <a href="http://www.occupythesec.org/files/SOX_Letter.pdf">submitted a letter</a> to Chairman Mary Schapiro of the SEC asking for a criminal enforcement action against JPMorgan and Jamie Dimon for violating Sarbanes-Oxley (SOX).  It has also submitted the letter to the House Financial Services Committee in connection with its <a href="http://www.occupythesec.org/files/HouseSOXLetter.pdf">hearing on the 10th anniversary of SOX.</a></li>

					<li>On June 22nd, 2012 Occupy the SEC <a href="http://www.occupythesec.org/files/LetterstoCongress.pdf">submitted commentary and a list of questions</a> to the House Financial Services Committee, relating to its hearing with JPMorgan Chase's CEO Jamie Dimon.</a></li>

					<li>On June 13th, 2012 Occupy the SEC <a href="http://www.occupythesec.org/files/DimonCommentLetter.pdf">submitted commentary and a list of questions</a> to the Senate Committee on Banking, Housing and Urban Affairs, relating to its hearing with JPMorgan Chase's CEO Jamie Dimon.</a></li>

					<li>On June 6th, 2012 Occupy the SEC <a href="http://www.occupythesec.org/files/Flyer.pdf">marched</a> on JP Morgan, the Federal Reserve and the SEC <a href="http://www.occupythesec.org/files/WhyWeMarch.pdf">asking for </a> a criminal investigation of JP Morgan's handling of its CIO office. The event was covered by <a href="http://fsrn.org/audio/activists-call-stricter-sec-enforcement-and-criminal-investigation-jp-morgan-chase/10405">Free Speech Radio News (FSRN)</a></li>


					<li>On Monday, April 23rd, Occupy the SEC held a <a href="http://www.occupythesec.org/files/April23rd2012VolckerBriefing.pdf">
						staff-only briefing on the Volcker Rule</a>. We also held a separate <a href="press/index.html">press briefing</a> at 4pm. Coverage of that event at <a href="http://www.youtube.com/watch?v=HoII0ywQtMQ&feature=player_embedded">RT</a> and <a href="http://www.washingtonpost.com/blogs/ezra-klein/post/occupy-the-regulatory-system/2012/04/27/gIQAjo21lT_blog.html">the Washington Post</a></li>
					<li>On Monday, February 13th, we <a href="http://www.nycga.net/events/event/march-to-the-fed-and-the-sec-enforce-the-volcker-rule/">
						marched to the NY Fed and the SEC</a> to ask them to Enforce a Strong Volcker Rule!</li>
